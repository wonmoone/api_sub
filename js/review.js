 $(function(){
  $(".comment_box > .btn > #hide_btn").hide();
    $(".comment_box > .btn > #show_btn").click(function(){
      $(".comment_box > .text").css({
        "height":"auto",
        "overflow":"block"
    });
    $(".comment_box").css({
      "height":"auto"
  });
    $(".comment_box > .btn > #hide_btn > img").css({
    "transform":"rotate(180deg)"
  });

  $(".comment_box > .btn > #hide_btn").show();
  $(".comment_box > .btn > #show_btn").hide();
    });
 });

